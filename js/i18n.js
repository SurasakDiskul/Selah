/* ---- Apply engine ---- */
window.__selahI18n = function () {
  'use strict';
  var LANG_KEY = 'selah_lang';
  var CMS_KEY = 'selah_cms_data';

  function getLang() { return localStorage.getItem(LANG_KEY) || 'en'; }
  function cmsOverrides() {
    try { return JSON.parse(localStorage.getItem(CMS_KEY)) || {}; } catch (e) { return {}; }
  }

  function value(lang, key) {
    var ov = cmsOverrides();
    var ok = 'i18n:' + lang + ':' + key;
    if (ov[ok] !== undefined) return ov[ok];           // admin override wins
    var dict = window.SELAH_I18N[lang] || {};
    if (dict[key] !== undefined) return dict[key];
    return (window.SELAH_I18N.en[key] !== undefined) ? window.SELAH_I18N.en[key] : '';
  }

  function apply(lang) {
    document.documentElement.setAttribute('lang', lang);
    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      var v = value(lang, el.getAttribute('data-i18n'));
      if (v !== '') el.innerHTML = v;
    });
    document.querySelectorAll('[data-i18n-ph]').forEach(function (el) {
      var v = value(lang, el.getAttribute('data-i18n-ph'));
      if (v !== '') el.setAttribute('placeholder', v);
    });
    // update toggle button label -> shows the language you can switch TO
    document.querySelectorAll('.lang-cur').forEach(function (el) {
      el.textContent = lang === 'en' ? 'EN' : 'TH';
    });
  }

  window.selahSetLang = function (lang) {
    localStorage.setItem(LANG_KEY, lang);
    apply(lang);
  };
  window.selahToggleLang = function () {
    window.selahSetLang(getLang() === 'en' ? 'th' : 'en');
  };
  window.selahGetLang = getLang;

  apply(getLang());
};
